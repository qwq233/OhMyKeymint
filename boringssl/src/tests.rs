#[test]
fn test_km_hkdf_vectors() {
    const IKM: [u8; 22] = [0x0b; 22];
    const SALT: [u8; 13] = [
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c,
    ];
    const INFO: [u8; 10] = [0xf0, 0xf1, 0xf2, 0xf3, 0xf4, 0xf5, 0xf6, 0xf7, 0xf8, 0xf9];
    const PRK: [u8; 32] = [
        0x07, 0x77, 0x09, 0x36, 0x2c, 0x2e, 0x32, 0xdf, 0x0d, 0xdc, 0x3f, 0x0d, 0xc4, 0x7b, 0xba,
        0x63, 0x90, 0xb6, 0xc7, 0x3b, 0xb5, 0x0f, 0x9c, 0x31, 0x22, 0xec, 0x84, 0x4a, 0xd7, 0xc2,
        0xb3, 0xe5,
    ];
    const OKM: [u8; 42] = [
        0x3c, 0xb2, 0x5f, 0x25, 0xfa, 0xac, 0xd5, 0x7a, 0x90, 0x43, 0x4f, 0x64, 0xd0, 0x36, 0x2f,
        0x2a, 0x2d, 0x2d, 0x0a, 0x90, 0xcf, 0x1a, 0x5a, 0x4c, 0x5d, 0xb0, 0x2d, 0x56, 0xec, 0xc4,
        0xc5, 0xbf, 0x34, 0x00, 0x72, 0x08, 0xd5, 0xb8, 0x87, 0x18, 0x58, 0x65,
    ];
    const LEGACY_PRK: [u8; 64] = [
        0xe9, 0xa8, 0xbc, 0x5d, 0x60, 0x4a, 0x4c, 0x4c, 0xf3, 0x14, 0x71, 0x4f, 0xb7, 0x53, 0x55,
        0xaf, 0x72, 0x7a, 0x5f, 0xeb, 0xfa, 0x8d, 0x1d, 0x4f, 0x9c, 0xba, 0x7e, 0x24, 0xda, 0x48,
        0x37, 0xc8, 0xb4, 0x2a, 0x90, 0xdf, 0xf8, 0x80, 0xb2, 0xa8, 0x8b, 0x15, 0x40, 0x7e, 0xca,
        0x93, 0x92, 0xba, 0x3d, 0xd2, 0xd2, 0x7a, 0x20, 0xfa, 0xd3, 0x8a, 0xe3, 0x4e, 0xa9, 0xb7,
        0xac, 0x95, 0x5a, 0x41,
    ];
    const LEGACY_OKM: [u8; 42] = [
        0xac, 0x9e, 0xef, 0xf8, 0xb0, 0xbf, 0x84, 0x21, 0xfc, 0x98, 0x7e, 0xa4, 0x83, 0xc6, 0xbb,
        0xd8, 0x4b, 0x77, 0x14, 0x36, 0x09, 0x06, 0x86, 0x97, 0x7f, 0xb0, 0xc9, 0x26, 0x67, 0x6a,
        0xbb, 0x7d, 0x91, 0xb8, 0x66, 0xd2, 0x13, 0x2d, 0x51, 0x69, 0xc2, 0x7f,
    ];

    let prk = km::hkdf_extract(&IKM, &SALT).unwrap();
    assert_eq!(&prk[..], &PRK);
    assert_eq!(&km::hkdf_expand(OKM.len(), &prk, &INFO).unwrap()[..], &OKM);

    let legacy_prk = km::omk_legacy_kdf_extract(&IKM, &SALT).unwrap();
    assert_eq!(&legacy_prk[..], &LEGACY_PRK);
    assert_eq!(
        &km::omk_legacy_kdf_expand(LEGACY_OKM.len(), &legacy_prk, &INFO).unwrap()[..],
        &LEGACY_OKM
    );
}

#[test]
fn test_x25519_agree_rfc7748() {
    use kmr_common::crypto::{ec as keymint_ec, Ec};

    const ALICE_PRIVATE: [u8; 32] = [
        0x77, 0x07, 0x6d, 0x0a, 0x73, 0x18, 0xa5, 0x7d, 0x3c, 0x16, 0xc1, 0x72, 0x51, 0xb2, 0x66,
        0x45, 0xdf, 0x4c, 0x2f, 0x87, 0xeb, 0xc0, 0x99, 0x2a, 0xb1, 0x77, 0xfb, 0xa5, 0x1d, 0xb9,
        0x2c, 0x2a,
    ];
    const ALICE_PUBLIC: [u8; 32] = [
        0x85, 0x20, 0xf0, 0x09, 0x89, 0x30, 0xa7, 0x54, 0x74, 0x8b, 0x7d, 0xdc, 0xb4, 0x3e, 0xf7,
        0x5a, 0x0d, 0xbf, 0x3a, 0x0d, 0x26, 0x38, 0x1a, 0xf4, 0xeb, 0xa4, 0xa9, 0x8e, 0xaa, 0x9b,
        0x4e, 0x6a,
    ];
    const BOB_PRIVATE: [u8; 32] = [
        0x5d, 0xab, 0x08, 0x7e, 0x62, 0x4a, 0x8a, 0x4b, 0x79, 0xe1, 0x7f, 0x8b, 0x83, 0x80, 0x0e,
        0xe6, 0x6f, 0x3b, 0xb1, 0x29, 0x26, 0x18, 0xb6, 0xfd, 0x1c, 0x2f, 0x8b, 0x27, 0xff, 0x88,
        0xe0, 0xeb,
    ];
    const BOB_PUBLIC: [u8; 32] = [
        0xde, 0x9e, 0xdb, 0x7d, 0x7b, 0x7d, 0xc1, 0xb4, 0xd3, 0x5b, 0x61, 0xc2, 0xec, 0xe4, 0x35,
        0x37, 0x3f, 0x83, 0x43, 0xc8, 0x5b, 0x78, 0x67, 0x4d, 0xad, 0xfc, 0x7e, 0x14, 0x6f, 0x88,
        0x2b, 0x4f,
    ];
    const SHARED_SECRET: [u8; 32] = [
        0x4a, 0x5d, 0x9d, 0x5b, 0xa4, 0xce, 0x2d, 0xe1, 0x72, 0x8e, 0x3b, 0xf4, 0x80, 0x35, 0x0f,
        0x25, 0xe0, 0x7e, 0x21, 0xc9, 0x47, 0xd1, 0x9e, 0x33, 0x76, 0xf0, 0x9b, 0x3c, 0x1e, 0x16,
        0x17, 0x42,
    ];
    const X25519_SPKI_PREFIX: [u8; 12] = [
        0x30, 0x2a, 0x30, 0x05, 0x06, 0x03, 0x2b, 0x65, 0x6e, 0x03, 0x21, 0x00,
    ];
    const ED25519_SPKI_PREFIX: [u8; 12] = [
        0x30, 0x2a, 0x30, 0x05, 0x06, 0x03, 0x2b, 0x65, 0x70, 0x03, 0x21, 0x00,
    ];

    let backend = ec::BoringEc::default();
    for (private_key, peer_public_key) in [(ALICE_PRIVATE, BOB_PUBLIC), (BOB_PRIVATE, ALICE_PUBLIC)]
    {
        let key = keymint_ec::Key::X25519(keymint_ec::X25519Key(private_key));
        let mut operation = backend.begin_agree(key.into()).unwrap();
        let mut peer_spki = X25519_SPKI_PREFIX.to_vec();
        peer_spki.extend_from_slice(&peer_public_key);
        operation.update(&peer_spki).unwrap();
        assert_eq!(operation.finish().unwrap(), SHARED_SECRET);
    }

    let key = keymint_ec::Key::X25519(keymint_ec::X25519Key(ALICE_PRIVATE));
    let mut operation = backend.begin_agree(key.into()).unwrap();
    let mut peer_spki = ED25519_SPKI_PREFIX.to_vec();
    peer_spki.extend_from_slice(&BOB_PUBLIC);
    operation.update(&peer_spki).unwrap();
    let error = operation.finish().unwrap_err();
    assert!(matches!(
        error.kind(),
        kmr_common::ErrorKind::Hal(kmr_wire::keymint::ErrorCode::InvalidArgument, _)
    ));
}
